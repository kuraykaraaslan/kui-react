function parseEnv(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Env file not found: ${filePath}`);
  }
  const out = {};
  const lines = fs.readFileSync(filePath, 'utf8').split('\n');
  for (const raw of lines) {
    const line = raw.trim();
    if (!line || line.startsWith('#')) continue;
    const eq = line.indexOf('=');
    if (eq < 0) continue;
    const key = line.slice(0, eq).trim();
    let value = line.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function buildConfigSource(env, sourceLabel) {
  const pick = (key, fallback) => {
    const v = env[key];
    return v && v.trim().length > 0 ? v : fallback;
  };
  const pickOptional = (key) => {
    const v = env[key];
    return v && v.trim().length > 0 ? v : null;
  };

  const brand = {
    short:       pick('NEXT_PUBLIC_BRAND_SHORT',       BRAND_DEFAULTS.NEXT_PUBLIC_BRAND_SHORT),
    name:        pick('NEXT_PUBLIC_BRAND_NAME',        BRAND_DEFAULTS.NEXT_PUBLIC_BRAND_NAME),
    initial:     pick('NEXT_PUBLIC_BRAND_INITIAL',     BRAND_DEFAULTS.NEXT_PUBLIC_BRAND_INITIAL),
    tagline:     pick('NEXT_PUBLIC_BRAND_TAGLINE',     BRAND_DEFAULTS.NEXT_PUBLIC_BRAND_TAGLINE),
    description: pick('NEXT_PUBLIC_BRAND_DESCRIPTION', BRAND_DEFAULTS.NEXT_PUBLIC_BRAND_DESCRIPTION),
  };

  const links = {
    siteUrl:    pick('NEXT_PUBLIC_SITE_URL',    BRAND_DEFAULTS.NEXT_PUBLIC_SITE_URL),
    github:     pick('NEXT_PUBLIC_GITHUB_URL',  BRAND_DEFAULTS.NEXT_PUBLIC_GITHUB_URL),
    authorName: pick('NEXT_PUBLIC_AUTHOR_NAME', BRAND_DEFAULTS.NEXT_PUBLIC_AUTHOR_NAME),
    authorUrl:  pick('NEXT_PUBLIC_AUTHOR_URL',  BRAND_DEFAULTS.NEXT_PUBLIC_AUTHOR_URL),
  };

  const buildColors = (suffix) => {
    const out = {};
    for (const [tsKey, envKey] of COLOR_KEYS) {
      out[tsKey] = pickOptional(`NEXT_PUBLIC_COLOR_${envKey}${suffix}`);
    }
    return out;
  };

  const lightColors = buildColors('');
  const darkColors = buildColors('_DARK');

  const lit = (v) => (v === null ? 'null' : JSON.stringify(v));
  const colorBlock = (colors) =>
    COLOR_KEYS.map(([key]) => `    ${key}: ${lit(colors[key])},`).join('\n');
  const tokenMapBlock = COLOR_KEYS
    .map(([key, , token]) => `  ['${key}', '${token}'],`)
    .join('\n');

  return `/**
 * Showcase configuration.
 *
 * Generated by scripts/env-to-showcase-config.js from ${sourceLabel}.
 * Do not edit by hand — re-run the script to regenerate.
 */

export const SHOWCASE_BRAND = {
  /** Short / package-style name (e.g. "KUI-react"). */
  short: ${lit(brand.short)},
  /** Display / wordmark name shown in UI (e.g. "KUIreact"). */
  name: ${lit(brand.name)},
  /** Initial used in the round logo badge. */
  initial: ${lit(brand.initial)},
  /** Short tagline. */
  tagline: ${lit(brand.tagline)},
  /** Long description used for meta tags and the home panel body. */
  description: ${lit(brand.description)},
} as const;

export const SHOWCASE_LINKS = {
  siteUrl: ${lit(links.siteUrl)},
  github: ${lit(links.github)},
  author: {
    name: ${lit(links.authorName)},
    url: ${lit(links.authorUrl)},
  },
} as const;

type ColorOverrides = {
  primary: string | null;
  primaryHover: string | null;
  primaryActive: string | null;
  primarySubtle: string | null;
  primaryFg: string | null;
  secondary: string | null;
  secondaryHover: string | null;
  secondaryActive: string | null;
  secondarySubtle: string | null;
  secondaryFg: string | null;
  surfaceBase: string | null;
  surfaceRaised: string | null;
  surfaceOverlay: string | null;
  surfaceSunken: string | null;
  border: string | null;
  borderStrong: string | null;
  borderFocus: string | null;
};

export const SHOWCASE_COLORS: { light: ColorOverrides; dark: ColorOverrides } = {
  light: {
${colorBlock(lightColors)}
  },
  dark: {
${colorBlock(darkColors)}
  },
};

const TOKEN_MAP: Array<[keyof ColorOverrides, string]> = [
${tokenMapBlock}
];

function declarations(overrides: ColorOverrides): string {
  return TOKEN_MAP.map(([key, token]) => {
    const v = overrides[key];
    return v ? \`  \${token}: \${v};\` : null;
  })
    .filter(Boolean)
    .join('\\n');
}

/**
 * Returns a CSS string with the active overrides, or an empty string when
 * no overrides are present.
 */
export function buildShowcaseColorCss(): string {
  const light = declarations(SHOWCASE_COLORS.light);
  const dark = declarations(SHOWCASE_COLORS.dark);
  const parts: string[] = [];
  if (light) parts.push(\`:root {\\n\${light}\\n}\`);
  if (dark) parts.push(\`.dark {\\n\${dark}\\n}\`);
  return parts.join('\\n');
}
`;
}

/**
 * Read envPath, render a fresh showcase.config.ts and write it to outPath.
 * sourceLabel is what gets baked into the file's banner comment (defaults to envPath basename).
 */
function bakeShowcaseConfig({ envPath, outPath, sourceLabel }) {
  const env = parseEnv(envPath);
  const label = sourceLabel || require('path').basename(envPath);
  const content = buildConfigSource(env, label);
  fs.writeFileSync(outPath, content, 'utf8');
}

module.exports = { bakeShowcaseConfig, parseEnv };
